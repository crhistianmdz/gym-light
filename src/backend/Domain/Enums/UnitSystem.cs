namespace GymFlow.Domain.Enums;

/// <summary>
/// Enum for unit systems used in anthropometric measurements.
/// </summary>
public enum UnitSystem
{
    /// <summary>
    /// Metric system (kilograms, centimeters).
    /// </summary>
    Metric = 0,

    /// <summary>
    /// Imperial system (pounds, inches).
    /// </summary>
    Imperial = 1
}